#include "fillit.h"

void	ft_put_map(t_data *data)
{
	int i;
	int j;

	i = -1;
	while (++i < data->size)
	{
		j = -1;
		while (++j < data->size)
			write(1, &data->map[i][j], 1);
		write(1, "\n", 1);
	}
}
